
public class Cuadrado {
	private char color;
	private byte posicion;
	private char cara;
	
	
	/**
	 * Este metodo crea uno de los nueve cuadrados que luego se van a imprimir (aun no se como)
	 * @param color     
	 * @param posicion    es la posición relativa a la cara del cuadradito
	 * @param cara        es la cara en la cual se encuentra el cuadradito (1-9 desde la
	 * esquina de la parte superior izquierda hasta la esquina inferior derecha)
	 */
	public Cuadrado (char color, byte posicion, char cara) {
		this.color = color;
		this.posicion = posicion;
		this.cara = cara;
	}
	
	
	
	
	
	
	
}
